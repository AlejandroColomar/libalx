/*
 * [[gnu::nonnull]]
 * int	alx_mallocs(void **restrict ptr, size_t size);
 */
#define alx_mallocs(ptr, size)	(					\
{									\
	auto	ptr_	= (ptr);					\
	void	*vp;							\
									\
	vp	= malloc(size);						\
	*ptr_	= static_cast<typeof(*ptr_)>(vp);			\
									\
	!(*ptr_);							\
}									\
)


/* Rename without alx_ prefix */
#if defined(ALX_NO_PREFIX)
#define mallocs(ptr, size)	alx_mallocs(ptr, size)
#endif


