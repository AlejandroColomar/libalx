#pragma once	/* libalx/base/string/strchr/strnrchrnul.h */


/******************************************************************************
 ******* headers **************************************************************
 ******************************************************************************/
#include <stddef.h>
#include <string.h>


/******************************************************************************
 ******* macros ***************************************************************
 ******************************************************************************/
/* Rename without alx_ prefix */
#if defined(ALX_NO_PREFIX)
#define strnrchrnul(size, str, c)	alx_strnrchrnul(size, str, c)
#endif


/******************************************************************************
 ******* enum *****************************************************************
 ******************************************************************************/


/******************************************************************************
 ******* struct / union *******************************************************
 ******************************************************************************/


/******************************************************************************
 ******* prototypes ***********************************************************
 ******************************************************************************/
__attribute__((nonnull, pure))
inline
ptrdiff_t alx_strnrchrnul	(ptrdiff_t size,
				 const char str[static restrict size],
				 char c);


/******************************************************************************
 ******* inline ***************************************************************
 ******************************************************************************/
inline
ptrdiff_t alx_strnrchrnul	(ptrdiff_t size,
				 const char str[static restrict size],
				 char c)
{
	const char	len = strnlen(str, size);
	const char	*p = memrchr(str, c, len);

	if (!p)
		return	len;
	return	p - str;
}


