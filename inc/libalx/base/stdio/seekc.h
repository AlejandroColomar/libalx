/******************************************************************************
 ******* inline functions *****************************************************
 ******************************************************************************/
inline
int	alx_seekc(char c)
{
	return	fseekc(stdin, c);
}

inline
int	alx_fseekc(FILE *stream, char c)
{
	int ch;

	do {
		ch = fgetc(stream);
	} while ((ch != c)  &&  (ch != EOF));

	return	(ch == EOF);
}

inline
int	alx_wait4enter(void)
{
	return	alx_seekc('\n');
}


