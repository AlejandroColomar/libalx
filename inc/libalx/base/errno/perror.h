#include <errno.h>
#include <stdio.h>
#include <string.h>


/******************************************************************************
 ******* macros ***************************************************************
 ******************************************************************************/
#define alx_perror(str)	do						\
{									\
	char	*str_	= str;						\
									\
	fprintf(stderr, "%s:%i: %s():\n", __FILE__, __LINE__, __func__);\
	if (str_)							\
		fprintf(stderr, "	%s\n", str_);			\
	fprintf(stderr, "E%i -	%s\n", errno, strerror(errno));		\
} while (0)


