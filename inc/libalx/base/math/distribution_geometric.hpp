/******************************************************************************
 ******* static inline functions (definitions) ********************************
 ******************************************************************************/
static inline
long double	alx_ldbl_distribution_geometric_E(long double p)
{

	if ((p < 0.0L) || (p > 1.0L)) {
		errno	= EDOM;
		return	nanl("");
	}

	return	1 / p;
}

static inline
double		alx_distribution_geometric_E	(double p)
{

	if ((p < 0.0) || (p > 1.0)) {
		errno	= EDOM;
		return	nan("");
	}

	return	1 / p;
}

static inline
float		alx_flt_distribution_geometric_E(float p)
{

	if ((p < 0.0f) || (p > 1.0f)) {
		errno	= EDOM;
		return	nanf("");
	}

	return	1 / p;
}


static inline
long double	alx_ldbl_distribution_geometric_Var(long double p)
{

	if ((p < 0.0L) || (p > 1.0L)) {
		errno	= EDOM;
		return	nanl("");
	}

	return	(1 - p) / (p * p);
}

static inline
double		alx_distribution_geometric_Var	(double p)
{

	if ((p < 0.0) || (p > 1.0)) {
		errno	= EDOM;
		return	nan("");
	}

	return	(1 - p) / (p * p);
}

static inline
float		alx_flt_distribution_geometric_Var(float p)
{

	if ((p < 0.0f) || (p > 1.0f)) {
		errno	= EDOM;
		return	nanf("");
	}

	return	(1 - p) / (p * p);
}


