#include <cassert>

#include <type_traits>


/******************************************************************************
 ******* macros ***************************************************************
 ******************************************************************************/
#define alx_static_assert_array(a)	do				\
{									\
									\
	static_assert(std::is_array <typeof(a)>::value, "Not a `[]`!");	\
} while (0)

#define alx_static_assert_char_array(a)	do				\
{									\
									\
	alx_static_assert_array(a);					\
	static_assert(std::is_same <char, typeof((a)[0])>::value,	\
						"Not a `char[]` !");	\
} while (0)


