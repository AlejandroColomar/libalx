#define	ALX_AVG(a, b) (							\
{									\
	((a) + (b)) / 2;						\
}									\
)

#define	ALX_AVGS(a, b) (						\
{									\
	__auto_type	a_	= (a);					\
	__auto_type	b_	= (b);					\
									\
	(a_ / 2) + (b_ / 2) + (((a_ % 2) + (b_ % 2)) / 2);		\
}									\
)


