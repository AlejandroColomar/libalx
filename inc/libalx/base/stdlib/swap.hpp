#define ALX_SWAP(a_ptr, b_ptr)	do					\
{									\
	auto			a_ptr_ = (a_ptr);			\
	auto			b_ptr_ = (b_ptr);			\
	__typeof__(*a_ptr_)	tmp_;					\
									\
	tmp_	= *a_ptr_;						\
	*a_ptr_	= *b_ptr_;						\
	*b_ptr_	= tmp_;							\
} while (0)


