#define ALX_SWAP(a_ptr, b_ptr)	do					\
{									\
	auto			_a_ptr = (a_ptr);			\
	auto			_b_ptr = (b_ptr);			\
	__typeof__(*_a_ptr)	_tmp;					\
									\
	_tmp	= *_a_ptr;						\
	*_a_ptr	= *_b_ptr;						\
	*_b_ptr	= _tmp;							\
} while (0)


