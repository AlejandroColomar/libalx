__attribute__((warn_unused_result))
inline
int	alx_strtoi_status	(int rstatus);


/******************************************************************************
 ******* inline functions *****************************************************
 ******************************************************************************/
inline
int	alx_strtoi_status	(int rstatus)
{

	switch (rstatus) {
	case 0:
		return	0;
	case ENOTSUP:
		return	rstatus;
	case ECANCELED:
	case EINVAL:
	case ERANGE:
	default:
		return	-rstatus;
	}
}


