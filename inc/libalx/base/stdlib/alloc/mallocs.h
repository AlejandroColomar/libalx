/******************************************************************************
 ******* include guard ********************************************************
 ******************************************************************************/
#ifndef ALX_STDLIB_ALLOC_MALLOCS_H
#define ALX_STDLIB_ALLOC_MALLOCS_H


/******************************************************************************
 ******* headers **************************************************************
 ******************************************************************************/
#include <errno.h>
#include <stddef.h>
#include <stdint.h>
#include <stdlib.h>


/******************************************************************************
 ******* macros ***************************************************************
 ******************************************************************************/
/*
 * int	alx_mallocs(type **restrict p, ptrdiff_t nmemb);
 */
#define alx_mallocs(ptr, nmemb)	(					\
{									\
	__label__	ret_;						\
	__auto_type	ptr_	= (ptr);				\
	int		err_;						\
									\
	err_	= 0;							\
	if (ptr_ == NULL) {						\
		errno	= EINVAL;					\
		err_	= EINVAL;					\
		goto ret_;						\
	}								\
									\
	*ptr_	= alx_mallocs__(nmemb, sizeof(**ptr_));			\
	if (!*ptr_)							\
		err_	= errno;					\
ret_:									\
	err_;								\
}									\
)


/******************************************************************************
 ******* enums ****************************************************************
 ******************************************************************************/


/******************************************************************************
 ******* structs / unions *****************************************************
 ******************************************************************************/


/******************************************************************************
 ******* variables ************************************************************
 ******************************************************************************/


/******************************************************************************
 ******* extern functions *****************************************************
 ******************************************************************************/


/******************************************************************************
 ******* inline functions (prototypes) ****************************************
 ******************************************************************************/
inline	void	*alx_mallocs__(ptrdiff_t nmemb, size_t size)
__attribute__((malloc));


/******************************************************************************
 ******* static inline functions (definitions) ********************************
 ******************************************************************************/
inline	void	*alx_mallocs__(ptrdiff_t nmemb, size_t size)
{

	if (nmemb < 0)
		goto ovf;
	if (nmemb > (PTRDIFF_MAX / (ptrdiff_t)size))
		goto ovf;

	return	malloc(size * nmemb);
ovf:
	errno	= EOVERFLOW;
	return	NULL;
}


/******************************************************************************
 ******* include guard ********************************************************
 ******************************************************************************/
#endif		/* libalx/base/stdlib/alloc/mallocs.h */


