long double	alx_ldbl_distribution_uniform_P	(long double a, long double b,
						long double x1, long double x2)
{

	if ((b < a) || (x1 < a) || (x2 > b) || (x2 < x1) || (b == a)) {
		errno	= EDOM;
		return	nanl("");
	}

	return	(x2 - x1) / (b - a);
}

