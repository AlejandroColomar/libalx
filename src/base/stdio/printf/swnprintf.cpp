int	swnprintf(char *restrict str, int *restrict written, size_t nmemb,
			char *restrict format, ...)
{
	va_list	ap;
	int	len;

	va_start(ap, format);
	len	= vsnprintf(str, nmemb, format, ap);
	va_end(ap);

	if (written != NULL)
		*written = len;

	if (len < 0)
		goto err;
	if ((unsigned)len >= nmemb)
		goto trunc;

	return	0;
err:
	return	-errno;
trunc:
	if (written)
		*written = nmemb - 1;
	errno	= ENOMEM;
	return	ENOMEM;
}


