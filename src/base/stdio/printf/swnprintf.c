int alx_swnprintf(char str[restrict], ptrdiff_t *restrict written,
		  ptrdiff_t nmemb, const char *restrict format, ...)
{
	va_list	ap;
	int	len;

	if (nmemb < 0)
		goto neg;

	va_start(ap, format);
	len	= vsnprintf(str, nmemb, format, ap);
	va_end(ap);

	if (written != NULL)
		*written = len;

	if (len < 0)
		goto err;
	if ((unsigned)len >= nmemb)
		goto trunc;

	return	0;
err:
	return	-errno;
trunc:
	if (written)
		*written = nmemb - 1;
	errno	= ENOMEM;
	return	ENOMEM;
neg:
	errno	= EOVERFLOW;
	return	-EOVERFLOW;
}

