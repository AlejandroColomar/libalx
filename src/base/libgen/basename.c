int	alx_basename_s	(char dest[static restrict FILENAME_MAX],
			 const char *restrict path)
{
	char	tmp[FILENAME_MAX];
	int	status;

	status	= alx_sbprintf(tmp, NULL, "%s", path);
	if (status)
		return	status;

	status	= alx_snprintfs(dest, NULL, FILENAME_MAX, "%s", basename(tmp));
	if (status)
		return	status;

	return	0;
}


